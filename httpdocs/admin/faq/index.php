		<h1 class="margin-bottom">FAQ</h1>

<p><strong>What is a MoBlog?</strong></p>

<p>Simply put, a MoBlog is a blog that you manage on our site, puckermob.com.  Your thoughts, your writing, your opinions, placed on our site. </p>

<p><strong>Why blog on PuckerMob</strong></p>

<p>If you love to write but don't do it often because it's hard to make money from it, Puckermob may be the solution you're looking for. </p>

<p>When you start your blog with us, you don't have to worry about installing as codes or anything like that. All you have to do is write great content and share it online. We'll pay you for each time your articles are shared on Facebook, twitter and other large social networks. </p>

<p>It's that easy! </p>

<p><strong>How will people find my content?</strong></p>

<p>Your articles will appear in our special MoBlog section of our site, accessible to the millions of readers that come to puckermob.com each month. Plus, in the near future, we’ll suggest blogger articles in association with related content on the site, based on the keywords you select (so choose your keywords carefully!)</p>

<p>But the biggest way people will find your articles is through social media, so make sure you share everything you write with your online networks. </p>

<p><strong>Can i write about anything I want?</strong></p>

<p>Yes…to a point. It’s important to read our terms of service to know what you can’t write about.  But in general, you can write about anything you want, as long as it’s not overly sexual or sexually graphic in nature, isn’t racist or bigoted, doesn’t incite people to commit crimes or acts of violence, isn’t deliberately insulting to a specific person, people or organization, doesn’t make unsubstantiated claims against a specific person, people or organization, or isn’t used to try to con people into sending you money, joining a pyramid scheme, etc.</p>

<p><strong>Do my articles have to be a specific length?</strong></p>

<p>Your articles have to be a minimum of 400 characters, and they must be in English. They can be as long as you’d like them to be.  However, we’ve found that articles that are about 500 – 1,000 words tend to be shared the most often.  </p>

<p>I already have a blog with content on it that isn’t being seen by anyone anymore. Can I simply republish those?</p>

<p>You can republish your own, original articles on your MoBlog.  What you can’t do is publish work stolen from other people’s sites, books, etc. All of the content that you post must either be your own work, or work that you have the rights to. </p>

<p><strong>How is the pay rate calculated?</strong></p>

<p>The pay rate changes each month, depending on a number of variables. Once you register, you’ll be able to see the rate of pay for each month.  </p>

<p>Currently, earnings are based on the following calculation:</p>

<p>Fixed monthly rate for social shares on certain networks x the percentage of viewers from the U.S. who have viewed your content.</p>

<p><strong>For example</strong>, let’s say that all of your articles together have received a total of 100,000 social shares, and the current monthly rate is $.02/share. Let’s also assume that 80% of your viewers are from the U.S.  your earnings would be the following:</p>

	<p>100,000 x $.02 = $2,000 x 0.80 = $1,600 </p>

<p><strong>How much can I make?</strong></p>

<p>How much you make really depends on you, your writing, and how much you share your work. </p>

<p>Better content, that's well written, entertaining and/or informative tend to do better. As do articles whose authors share then more aggressively. The more people like your articles and can relate to them, the more they'll share them. The more they share them, the more money you make. </p>

<p><strong>When will I get paid?</strong></p>

<p>Assuming we have all of your necessary tax information, payments get made between the 15th – 17th of each month, approximately 45 days after the end of each month.  </p>

<p><strong>For example</strong>, payments for the month of April would be made between June 15th – June 17th. </p>

<p>Please note, however: your account must meet a minimum threshold of $25 before we can pay you. </p>

<p><strong>I don’t have a PayPal account – can I get paid another way?</strong></p>

<p>For right now, the only way we can make payments is through PayPal. We are working on establishing other methods of payment in the near future.  </p>

<p><strong>Why does the earnings page on my dashboard show a lot fewer social shares than the public pages where my content is published?</strong></p>

<p>The “shares” that you see on the public pages include facebook “likes” and comments. While these are nice to get, they do very little to help us generate more traffic to the site.  So we only pay for the times people have actually shared your article with other people, and don’t include “likes” or comments in the calculation. </p>

<p><strong>How do you know how many times my article has been shared?</strong></p>

<p>We don’t do the calculation of shares ourselves. Instead, we use an independent source, called SharedCount (http:www.sharedcount.com) to measure the number of shares each article receives. </p>

<p>You’re welcomed to use that site as well – simply copy the URL of your articles and place it into SharedCount to see the number of shares your articles have received.  </p>

<p><strong>What social networks are included in the calculation of my shares?</strong></p>

<p>Currently, sharedcount calculates the shares for the following networks:</p>

<ul>
	<li>Facebook</li>
	<li>Twitter</li>
	<li>Pinterest</li>
	<li>LinkedIn</li>
	<li>StumbleUpon</li>
	<li>Google +</li>
</ul>

<p>Shares on any other network, including Reddit, Digg and Delicious will not be counted toward your earnings total.</p>

